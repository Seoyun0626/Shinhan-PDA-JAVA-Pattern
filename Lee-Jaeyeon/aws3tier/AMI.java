package aws3tier;

/**
 * An enum representing the types of AMIs (Amazon Machine Images) offered by AWS.
 *
 */
public enum AMI {

    /**
     * Amazon Linux AMI
     */
    AMAZON_LINUX,

    /**
     * macOS AMI
     */
    MAC_OS,

    /**
     * Ubuntu AMI
     */
    UBUNTU,

    /**
     * Windows AMI
     */
    WINDOWS,

    /**
     * Red Hat Enterprise Linux AMI
     */
    REDHAT,

    /**
     * SUSE Linux Enterprise Server AMI
     */
    SUSE,

    /**
     * Debian GNU/Linux AMI
     */
    DEBIAN
}
